package com.sandol.app.vo;

public class InquiryVO {
	private String inquiryUserId;
	private String inquiryUserName;
	private String inquiryTitle;
	private String inquiryMemo;
	private String inquiryDatetime;
	private String inquiryAnswer;
	
	public String getInquiryUserId() {
		return inquiryUserId;
	}
	public void setInquiryUserId(String inquiryUserId) {
		this.inquiryUserId = inquiryUserId;
	}
	public String getInquiryUserName() {
		return inquiryUserName;
	}
	public void setInquiryUserName(String inquiryUserName) {
		this.inquiryUserName = inquiryUserName;
	}
	public String getInquiryTitle() {
		return inquiryTitle;
	}
	public void setInquiryTitle(String inquiryTitle) {
		this.inquiryTitle = inquiryTitle;
	}
	public String getInquiryMemo() {
		return inquiryMemo;
	}
	public void setInquiryMemo(String inquiryMemo) {
		this.inquiryMemo = inquiryMemo;
	}
	public String getInquiryDatetime() {
		return inquiryDatetime;
	}
	public void setInquiryDatetime(String inquiryDatetime) {
		this.inquiryDatetime = inquiryDatetime;
	}
	public String getInquiryAnswer() {
		return inquiryAnswer;
	}
	public void setInquiryAnswer(String inquiryAnswer) {
		this.inquiryAnswer = inquiryAnswer;
	}
}
